#!/usr/bin/env node
/**
 * Provision an ephemeral `.dev.vars` for E2E runs (CI + local).
 *
 * `wrangler dev` (booted by playwright.config.ts's `webServer`) reads secrets
 * from `.dev.vars`; the Playwright specs read `SETUP_CODE` from the same file
 * via `tests/helpers/dev-vars.ts`. A fresh checkout / worktree has no
 * `.dev.vars`, so the worker boots without JWT keys and the `/api/auth/setup`
 * step fails (`setup_code_unset`) — which is why the seeded E2E suite could not
 * run in CI.
 *
 * This generates a throwaway ES256 keypair + a random JWT_SECRET + a random
 * SETUP_CODE and writes them as SINGLE-LINE values (the worker's PEM parser,
 * `server/lib/jwt-keyring.ts` pemToBuf, strips all whitespace, and the test
 * helper parses `.dev.vars` line-by-line — so multi-line PEM would break it).
 *
 * IDEMPOTENT: never overwrites an existing `.dev.vars` (respects a real local
 * one). All values are freshly generated — no secret is hard-coded.
 */
import { generateKeyPairSync, randomBytes } from 'node:crypto';
import { existsSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const root = process.cwd();
const devVarsPath = resolve(root, '.dev.vars');

if (existsSync(devVarsPath)) {
  console.log('✓  .dev.vars already present — left untouched.');
  process.exit(0);
}

// ES256 (P-256) keypair — PKCS8 private (sign) + SPKI public (verify), PEM.
const { publicKey, privateKey } = generateKeyPairSync('ec', {
  namedCurve: 'P-256',
  publicKeyEncoding: { type: 'spki', format: 'pem' },
  privateKeyEncoding: { type: 'pkcs8', format: 'pem' },
});

// Collapse to a single line: the keyring strips all whitespace before base64
// decode, and `.dev.vars` is parsed line-by-line, so newlines must not survive.
const oneLine = (pem) => pem.replace(/\r?\n/g, '');

const lines = [
  '# Generated by scripts/gen-e2e-dev-vars.mjs for E2E runs — throwaway, gitignored.',
  // The E2E specs post a fixed `verificationCode: '000000'` to /api/auth/setup,
  // so the worker's SETUP_CODE must match that fixture (not a random value) or
  // every workspace-init call is rejected and login fails. This is a test
  // fixture, not a secret.
  'SETUP_CODE=000000',
  `JWT_SECRET=${randomBytes(32).toString('hex')}`,
  `JWT_PRIVATE_KEY_V1=${oneLine(privateKey)}`,
  `JWT_PUBLIC_KEY_V1=${oneLine(publicKey)}`,
  '',
];

writeFileSync(devVarsPath, lines.join('\n'), 'utf8');
console.log('✓  wrote ephemeral .dev.vars (SETUP_CODE + JWT_SECRET + ES256 v1 keypair).');
