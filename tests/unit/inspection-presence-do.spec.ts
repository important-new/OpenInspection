import { describe, it, expect } from 'vitest';
import { readFileSync } from 'node:fs';
import { resolve } from 'node:path';

describe('Presence Durable Objects — smoke', () => {
    it('InspectionPresenceDO class name matches wrangler binding', () => {
        const src = readFileSync(resolve(__dirname, '../../server/durable-objects/inspection-presence.ts'), 'utf8');
        expect(src).toMatch(/export class InspectionPresenceDO/);
    });

    it('TenantPresenceDO class name matches wrangler binding', () => {
        const src = readFileSync(resolve(__dirname, '../../server/durable-objects/tenant-presence.ts'), 'utf8');
        expect(src).toMatch(/export class TenantPresenceDO/);
    });

    // Read the .example template (committed) rather than the populated
    // wrangler.jsonc (gitignored, generated by setup). A fresh clone has no
    // wrangler.jsonc until setup runs, but the structural check needs to
    // pass in CI / `npm test` without any setup prerequisite.
    it('wrangler.jsonc binds both classes', () => {
        const cfg = readFileSync(resolve(__dirname, '../../wrangler.jsonc'), 'utf8');
        expect(cfg).toMatch(/"class_name":\s*"InspectionPresenceDO"/);
        expect(cfg).toMatch(/"class_name":\s*"TenantPresenceDO"/);
    });
});
